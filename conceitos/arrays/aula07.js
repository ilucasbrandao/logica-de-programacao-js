// Dado o array abaixo, exiba no console o elemento que está no meio do array.

const arr = [4, 8, "Olá", true, 28, "45", "JavaScript", 50];

let meio = Math.floor(arr.length / 2);

console.log(`O elemento que está no meio do array é: ${arr[meio]}`);
